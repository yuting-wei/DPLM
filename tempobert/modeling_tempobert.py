class TimeSelfAttention(nn.Module):
    def __init__(self, config, SPECIAL_TIMES_COUNT):
        super().__init__()

        self.dropout = nn.Dropout(config.attention_probs_dropout_prob)
        self.hidden_size = config.hidden_size
        self.distance_embedding = nn.Embedding(
            2 * (SPECIAL_TIMES_COUNT + len(config.times)) - 1, config.hidden_size
        )
        self.time_embeddings = nn.Embedding(
                    SPECIAL_TIMES_COUNT + len(config.times), config.hidden_size
                )
        self.att_time_embedding_layer = nn.Embedding(SPECIAL_TIMES_COUNT + len(config.times), config.hidden_size)
        self.output = TimeBertSelfOutput(config)

    def forward(self, time_ids):
        hidden_states = self.time_embeddings.weight

        key_layer = hidden_states
        value_layer = hidden_states
        query_layer = hidden_states

        # Take the dot product between "query" and "key" to get the raw attention scores.
        attention_scores = torch.matmul(query_layer, key_layer.T)

        time_length = hidden_states.size()[0]
        position_ids_l = torch.arange(
            time_length, dtype=torch.long, device=hidden_states.device
        ).view(-1, 1)
        position_ids_r = torch.arange(
            time_length, dtype=torch.long, device=hidden_states.device
        ).view(1, -1)
        distance = position_ids_l - position_ids_r
        time_positional_embedding = self.distance_embedding(
            distance + time_length - 1
        )
        time_positional_embedding = time_positional_embedding.to(
            dtype=query_layer.dtype
        )  # fp16 compatibility

        # if self.position_embedding_type == "relative_key":
        # relative_position_scores = torch.einsum(
        #     "ld,lrd->lr", query_layer, time_positional_embedding
        # )
        # attention_scores = attention_scores + relative_position_scores
        # elif self.position_embedding_type == "relative_key_query":
        relative_position_scores_query = torch.einsum(
            "ld,lrd->lr", query_layer, time_positional_embedding
        )
        relative_position_scores_key = torch.einsum(
            "rd,lrd->lr", key_layer, time_positional_embedding
        )
        attention_scores = (
            attention_scores
            + relative_position_scores_query
            + relative_position_scores_key
        )

        attention_scores = attention_scores / math.sqrt(self.hidden_size)

        # Normalize the attention scores to probabilities.
        attention_probs = nn.Softmax(dim=-1)(attention_scores)

        # This is actually dropping out entire tokens to attend to, which might
        # seem a bit unusual, but is taken from the original Transformer paper.
        attention_probs = self.dropout(attention_probs)

        context_layer = torch.matmul(attention_probs, value_layer) #  attention_probs: [time_length, time_length] value_layer: [time_length, hidden_size]
        
        output_attentions = False
        self_outputs = (
            (context_layer, attention_probs) if output_attentions else (context_layer,)
        )

        attention_output = self.output(self_outputs[0], hidden_states)
        # self.att_time_embedding_layer.weight.data = attention_output
        
        self.att_time_embedding_layer.weight = torch.nn.Parameter(attention_output)

        time_embeddings = self.att_time_embedding_layer(time_ids)

        # outputs = (attention_output,) + self_outputs[
        #     1:
        # ] 
        return time_embeddings # [time_embedding_length, hidden_size]
        # return outputs # ([time_embedding_length, hidden_size], attention_probs)

class TempoBertEmbeddings(nn.Module):
    """Construct the embeddings from word, position, token_type and time embeddings."""

    def __init__(self, config):
        super().__init__()
        self.word_embeddings = nn.Embedding(
            config.vocab_size, config.hidden_size, padding_idx=config.pad_token_id
        )
        self.position_embeddings = nn.Embedding(
            config.max_position_embeddings, config.hidden_size
        )
        self.token_type_embeddings = nn.Embedding(
            config.type_vocab_size, config.hidden_size
        )

        # self.LayerNorm is not snake-cased to stick with TensorFlow model variable name and be able to load
        # any TensorFlow checkpoint file
        self.LayerNorm = nn.LayerNorm(config.hidden_size, eps=config.layer_norm_eps)
        self.dropout = nn.Dropout(config.hidden_dropout_prob)
        # position_ids (1, len position emb) is contiguous in memory and exported when serialized
        self.position_embedding_type = getattr(
            config, "position_embedding_type", "absolute"
        )
        self.register_buffer(
            "position_ids", torch.arange(config.max_position_embeddings).expand((1, -1))
        )
        if version.parse(torch.__version__) > version.parse("1.6.0"):
            self.register_buffer(
                "token_type_ids",
                torch.zeros(
                    self.position_ids.size(),
                    dtype=torch.long,
                    device=self.position_ids.device,
                ),
                persistent=False,
            )

        self.init_time_embeddings(config)

    def init_time_embeddings(self, config):
        self.times = config.times
        self.time_embedding_type = getattr(
            config, "time_embedding_type", "temporal_attention"
        )

        if utils.is_time_id_necessary(self.time_embedding_type):
            # time_ids is a range (1, len time emb)
            time_ids = torch.arange(len(self.times)).expand((1, -1))
            SPECIAL_TIMES_COUNT = 2  # NOTE: hardcoded (see TempoSpecialTokensMixin)
            if "attention" in self.time_embedding_type:
                # NOTE: each time (including "special" times) has a single embedding (for all tokens in the vocabulary)
                # self.time_embeddings = TimeSelfAttention(config, SPECIAL_TIMES_COUNT)  
                self.time_embeddings = Time2Sin(config)
                # 改这里 每个timeembedding由word对应的freq和 timeid 组成
                # self.time_embeddings = self.time_embeddings()
                # self.time_embeddings = nn.Embedding(
                #     SPECIAL_TIMES_COUNT + len(self.times), config.hidden_size
                # )

            # time_ids is contiguous in memory and exported when serialized
            self.register_buffer("time_ids", time_ids)

    @classmethod
    def from_non_temporal(cls, embeddings, config):
        embeddings.__class__ = cls
        embeddings.init_time_embeddings(config)
        return embeddings

    def forward(
        self,
        input_ids=None,
        token_type_ids=None,
        position_ids=None,
        time_ids=None,
        inputs_embeds=None,
        past_key_values_length=0,
    ):
        if input_ids is not None:
            input_shape = input_ids.size()
        else:
            input_shape = inputs_embeds.size()[:-1]

        seq_length = input_shape[1]

        if position_ids is None:
            position_ids = self.position_ids[
                :, past_key_values_length : seq_length + past_key_values_length
            ]

        # Setting the token_type_ids to the registered buffer in constructor where it is all zeros, which usually occurs
        # when its auto-generated, registered buffer helps users when tracing the model without passing token_type_ids, solves
        # issue #5664
        if token_type_ids is None:
            if hasattr(self, "token_type_ids"):
                buffered_token_type_ids = self.token_type_ids[:, :seq_length]
                buffered_token_type_ids_expanded = buffered_token_type_ids.expand(
                    input_shape[0], seq_length
                )
                token_type_ids = buffered_token_type_ids_expanded
            else:
                token_type_ids = torch.zeros(
                    input_shape, dtype=torch.long, device=self.position_ids.device
                )

        if time_ids is None and utils.is_time_id_necessary(self.time_embedding_type):
            time_ids = self.time_ids[:, :seq_length]

        if inputs_embeds is None:
            inputs_embeds = self.word_embeddings(input_ids)
        token_type_embeddings = self.token_type_embeddings(token_type_ids)

        embeddings = inputs_embeds + token_type_embeddings
        if self.position_embedding_type == "absolute":
            position_embeddings = self.position_embeddings(position_ids)
            embeddings += position_embeddings
        time_embeddings = None
        if "attention" in self.time_embedding_type:
            # Get the time embedding per token
            # time_embeddings = self.time_embeddings(time_ids)
            time_embeddings = self.time_embeddings(inputs_embeds, time_ids.unsqueeze(-1))
            # print(time_ids.size())
            # time_embeddings = time_ids.unsqueeze(-1)

        embeddings = self.LayerNorm(embeddings)
        embeddings = self.dropout(embeddings)
        return (
            (embeddings, time_embeddings)
            if "attention" in self.time_embedding_type
            else embeddings
        )


class BertSelfAttention(nn.Module):
    def __init__(self, config):
        super().__init__()
        if config.hidden_size % config.num_attention_heads != 0 and not hasattr(
            config, "embedding_size"
        ):
            raise ValueError(
                "The hidden size (%d) is not a multiple of the number of attention "
                "heads (%d)" % (config.hidden_size, config.num_attention_heads)
            )

        self.num_attention_heads = config.num_attention_heads
        self.attention_head_size = int(config.hidden_size / config.num_attention_heads)
        self.all_head_size = self.num_attention_heads * self.attention_head_size

        self.query = nn.Linear(config.hidden_size, self.all_head_size)
        self.key = nn.Linear(config.hidden_size, self.all_head_size)
        self.value = nn.Linear(config.hidden_size, self.all_head_size)

        self.dropout = nn.Dropout(config.attention_probs_dropout_prob)
        self.position_embedding_type = getattr(
            config, "position_embedding_type", "absolute"
        )
        if (
            self.position_embedding_type == "relative_key"
            or self.position_embedding_type == "relative_key_query"
        ):
            self.max_position_embeddings = config.max_position_embeddings
            self.distance_embedding = nn.Embedding(
                2 * config.max_position_embeddings - 1, self.attention_head_size
            )

        self.is_decoder = config.is_decoder

    def transpose_for_scores(self, x):
        new_x_shape = x.size()[:-1] + (
            self.num_attention_heads,
            self.attention_head_size,
        )
        x = x.view(*new_x_shape)
        return x.permute(0, 2, 1, 3)

    def forward(
        self,
        hidden_states,
        attention_mask=None,
        head_mask=None,
        encoder_hidden_states=None,
        encoder_attention_mask=None,
        past_key_value=None,
        output_attentions=False,
    ):
        mixed_query_layer = self.query(hidden_states)

        # If this is instantiated as a cross-attention module, the keys
        # and values come from an encoder; the attention mask needs to be
        # such that the encoder's padding tokens are not attended to.
        is_cross_attention = encoder_hidden_states is not None

        if is_cross_attention and past_key_value is not None:
            # reuse k,v, cross_attentions
            key_layer = past_key_value[0]
            value_layer = past_key_value[1]
            attention_mask = encoder_attention_mask
        elif is_cross_attention:
            key_layer = self.transpose_for_scores(self.key(encoder_hidden_states))
            value_layer = self.transpose_for_scores(self.value(encoder_hidden_states))
            attention_mask = encoder_attention_mask
        elif past_key_value is not None:
            key_layer = self.transpose_for_scores(self.key(hidden_states))
            value_layer = self.transpose_for_scores(self.value(hidden_states))
            key_layer = torch.cat([past_key_value[0], key_layer], dim=2)
            value_layer = torch.cat([past_key_value[1], value_layer], dim=2)
        else:
            key_layer = self.transpose_for_scores(self.key(hidden_states))
            value_layer = self.transpose_for_scores(self.value(hidden_states))

        query_layer = self.transpose_for_scores(mixed_query_layer)

        if self.is_decoder:
            # if cross_attention save Tuple(torch.Tensor, torch.Tensor) of all cross attention key/value_states.
            # Further calls to cross_attention layer can then reuse all cross-attention
            # key/value_states (first "if" case)
            # if uni-directional self-attention (decoder) save Tuple(torch.Tensor, torch.Tensor) of
            # all previous decoder key/value_states. Further calls to uni-directional self-attention
            # can concat previous decoder key/value_states to current projected key/value_states (third "elif" case)
            # if encoder bi-directional self-attention `past_key_value` is always `None`
            past_key_value = (key_layer, value_layer)

        # Take the dot product between "query" and "key" to get the raw attention scores.
        attention_scores = torch.matmul(query_layer, key_layer.transpose(-1, -2))

        if (
            self.position_embedding_type == "relative_key"
            or self.position_embedding_type == "relative_key_query"
        ):
            seq_length = hidden_states.size()[1]
            position_ids_l = torch.arange(
                seq_length, dtype=torch.long, device=hidden_states.device
            ).view(-1, 1)
            position_ids_r = torch.arange(
                seq_length, dtype=torch.long, device=hidden_states.device
            ).view(1, -1)
            distance = position_ids_l - position_ids_r
            positional_embedding = self.distance_embedding(
                distance + self.max_position_embeddings - 1
            )
            positional_embedding = positional_embedding.to(
                dtype=query_layer.dtype
            )  # fp16 compatibility

            if self.position_embedding_type == "relative_key":
                relative_position_scores = torch.einsum(
                    "bhld,lrd->bhlr", query_layer, positional_embedding
                )
                attention_scores = attention_scores + relative_position_scores
            elif self.position_embedding_type == "relative_key_query":
                relative_position_scores_query = torch.einsum(
                    "bhld,lrd->bhlr", query_layer, positional_embedding
                )
                relative_position_scores_key = torch.einsum(
                    "bhrd,lrd->bhlr", key_layer, positional_embedding
                )
                attention_scores = (
                    attention_scores
                    + relative_position_scores_query
                    + relative_position_scores_key
                )

        attention_scores = attention_scores / math.sqrt(self.attention_head_size)
        if attention_mask is not None:
            # Apply the attention mask is (precomputed for all layers in BertModel forward() function)
            attention_scores = attention_scores + attention_mask

        # Normalize the attention scores to probabilities.
        attention_probs = nn.Softmax(dim=-1)(attention_scores)

        # This is actually dropping out entire tokens to attend to, which might
        # seem a bit unusual, but is taken from the original Transformer paper.
        attention_probs = self.dropout(attention_probs)

        # Mask heads if we want to
        if head_mask is not None:
            attention_probs = attention_probs * head_mask

        context_layer = torch.matmul(attention_probs, value_layer)

        context_layer = context_layer.permute(0, 2, 1, 3).contiguous()
        new_context_layer_shape = context_layer.size()[:-2] + (self.all_head_size,)
        context_layer = context_layer.view(*new_context_layer_shape)

        outputs = (
            (context_layer, attention_probs) if output_attentions else (context_layer,)
        )

        if self.is_decoder:
            outputs = outputs + (past_key_value,)
        return outputs

